<#
.SYNOPSIS
    Unified launcher for Dirigent - starts backend and frontend with one command.

.DESCRIPTION
    Automatically:
    1. Activates the Python virtual environment
    2. Starts the backend (or reuses existing instance)
    3. Waits for backend health
    4. Launches the frontend

.NOTES
    Windows-first implementation. Cross-platform structure for future Linux/macOS support.
#>

param(
    [switch]$SkipFrontend,
    [switch]$BackendOnly
)

# Set error handling
$ErrorActionPreference = "Stop"

# ============================================================================
# CONFIGURATION
# ============================================================================
$SCRIPT_DIR = Split-Path -Parent $MyInvocation.MyCommand.Path
$PROJECT_ROOT = $SCRIPT_DIR
$VENV_PATH = Join-Path $PROJECT_ROOT ".venv"
$VENV_PYTHON = Join-Path $VENV_PATH "Scripts\python.exe"
$BACKEND_SCRIPT = Join-Path $PROJECT_ROOT "run_backend.py"
$FRONTEND_SCRIPT = Join-Path $PROJECT_ROOT "run_frontend.bat"
$LOG_DIR = Join-Path $PROJECT_ROOT "logs"
$BACKEND_LOG = Join-Path $LOG_DIR "backend.log"
$HEALTH_URL = "http://127.0.0.1:8000/health"
$HEALTH_TIMEOUT_SECONDS = 30
$HEALTH_POLL_INTERVAL_MS = 500

# ============================================================================
# HELPER FUNCTIONS
# ============================================================================
function Write-ProgressStep {
    param(
        [int]$Step,
        [int]$Total,
        [string]$Message
    )
    $percentage = [math]::Round(($Step / $Total) * 100)
    Write-Host "[$Step/$Total] $Message" -ForegroundColor Cyan
}

function Write-Status {
    param([string]$Message)
    Write-Host $Message -ForegroundColor Gray
}

function Write-Success {
    param([string]$Message)
    Write-Host $Message -ForegroundColor Green
}

function Write-Error {
    param([string]$Message)
    Write-Host "ERROR: $Message" -ForegroundColor Red
}

function Test-BackendHealth {
    try {
        $response = Invoke-WebRequest -Uri $HEALTH_URL -UseBasicParsing -TimeoutSec 2 -ErrorAction SilentlyContinue
        return $response.StatusCode -eq 200
    } catch {
        return $false
    }
}

function Wait-ForBackendHealth {
    param(
        [int]$TimeoutSeconds = $HEALTH_TIMEOUT_SECONDS
    )
    
    $deadline = (Get-Date).AddSeconds($TimeoutSeconds)
    $attempt = 0
    
    Write-Status "Polling $HEALTH_URL (timeout: ${TimeoutSeconds}s)"
    
    while ((Get-Date) -lt $deadline) {
        $attempt++
        
        if (Test-BackendHealth) {
            Write-Success "Backend is healthy (attempt $attempt)"
            return $true
        }
        
        Write-Status "  Attempt $attempt: not ready yet..."
        Start-Sleep -Milliseconds $HEALTH_POLL_INTERVAL_MS
    }
    
    Write-Error "Backend did not become healthy within $TimeoutSeconds seconds"
    return $false
}

# ============================================================================
# MAIN EXECUTION
# ============================================================================
try {
    Write-Host ""
    Write-Host "========================================" -ForegroundColor Magenta
    Write-Host "  Dirigent Launcher" -ForegroundColor Magenta
    Write-Host "========================================" -ForegroundColor Magenta
    Write-Host ""
    
    # Change to project root
    Set-Location $PROJECT_ROOT
    Write-Status "Working directory: $PROJECT_ROOT"
    Write-Host ""
    
    # [1/4] Activate virtual environment
    Write-ProgressStep -Step 1 -Total 4 -Message "Activating virtual environment"
    
    if (-not (Test-Path $VENV_PATH)) {
        Write-Error "Virtual environment not found at: $VENV_PATH"
        Write-Host "Please create a virtual environment first:" -ForegroundColor Yellow
        Write-Host "  python -m venv .venv" -ForegroundColor Yellow
        Write-Host "  .venv\Scripts\activate" -ForegroundColor Yellow
        Write-Host "  pip install -r backend\requirements.txt" -ForegroundColor Yellow
        exit 1
    }
    
    if (-not (Test-Path $VENV_PYTHON)) {
        Write-Error "Python executable not found in virtual environment: $VENV_PYTHON"
        Write-Host "The virtual environment may be corrupted. Try recreating it:" -ForegroundColor Yellow
        Write-Host "  Remove-Item .venv -Recurse -Force" -ForegroundColor Yellow
        Write-Host "  python -m venv .venv" -ForegroundColor Yellow
        exit 1
    }
    
    $pythonExe = $VENV_PYTHON
    Write-Success "Virtual environment found: $VENV_PATH"
    Write-Host ""
    
    # [2/4] Start backend
    Write-ProgressStep -Step 2 -Total 4 -Message "Starting backend"
    
    # Check if backend is already running
    $backendAlreadyRunning = $false
    if (Test-BackendHealth) {
        Write-Success "Backend is already running and healthy"
        $backendAlreadyRunning = $true
    } else {
        Write-Status "Backend is not running. Starting new instance..."
        
        # Ensure log directory exists
        if (-not (Test-Path $LOG_DIR)) {
            New-Item -Path $LOG_DIR -ItemType Directory -Force | Out-Null
            Write-Status "Created log directory: $LOG_DIR"
        }
        
        # Start backend process
        Write-Status "Executing: $pythonExe $BACKEND_SCRIPT --no-reload"
        Write-Status "Log file: $BACKEND_LOG"
        
        $processInfo = New-Object System.Diagnostics.ProcessStartInfo
        $processInfo.FileName = $pythonExe
        $processInfo.Arguments = "`"$BACKEND_SCRIPT`" --no-reload"
        $processInfo.WorkingDirectory = $PROJECT_ROOT
        $processInfo.UseShellExecute = $false
        $processInfo.RedirectStandardOutput = $true
        $processInfo.RedirectStandardError = $true
        $processInfo.CreateNoWindow = $true
        
        # Set environment variables
        $processInfo.EnvironmentVariables["API_PORT"] = "8000"
        $processInfo.EnvironmentVariables["DIRIGENT_DEV"] = "0"
        
        $backendProcess = [System.Diagnostics.Process]::Start($processInfo)
        
        # Start log tailing in background
        $logTailJob = Start-Job -ScriptBlock {
            param($logFile, $process)
            
            while (-not $process.HasExited) {
                if (Test-Path $logFile) {
                    Get-Content $logFile -Tail 10 -ErrorAction SilentlyContinue | ForEach-Object {
                        Write-Host "  [BACKEND] $_" -ForegroundColor DarkGray
                    }
                }
                Start-Sleep -Seconds 2
            }
        } -ArgumentList $BACKEND_LOG, $backendProcess
        
        Write-Success "Backend process started (PID: $($backendProcess.Id))"
    }
    Write-Host ""
    
    # [3/4] Wait for backend health
    if (-not $backendAlreadyRunning) {
        Write-ProgressStep -Step 3 -Total 4 -Message "Waiting for backend health"
        
        $healthy = Wait-ForBackendHealth -TimeoutSeconds $HEALTH_TIMEOUT_SECONDS
        
        if (-not $healthy) {
            Write-Error "Backend failed to start properly"
            
            # Check if process is still running
            if (-not $backendProcess.HasExited) {
                Write-Status "Terminating backend process..."
                $backendProcess.Kill()
                $backendProcess.WaitForExit(5000) | Out-Null
            }
            
            # Show recent log output
            Write-Host ""
            Write-Host "Recent backend log output:" -ForegroundColor Yellow
            if (Test-Path $BACKEND_LOG) {
                Get-Content $BACKEND_LOG -Tail 20 | ForEach-Object {
                    Write-Host "  $_" -ForegroundColor DarkGray
                }
            }
            
            exit 1
        }
        
        Write-Success "Backend is healthy and ready"
    } else {
        Write-ProgressStep -Step 3 -Total 4 -Message "Backend already healthy (skipped)"
    }
    Write-Host ""
    
    # [4/4] Launch frontend
    if ($SkipFrontend -or $BackendOnly) {
        Write-ProgressStep -Step 4 -Total 4 -Message "Skipping frontend (--SkipFrontend or --BackendOnly specified)"
        Write-Host ""
        Write-Success "Backend is running at: $HEALTH_URL"
        Write-Success "Dirigent backend is ready."
        
        if (-not $backendAlreadyRunning) {
            Write-Host ""
            Write-Host "Press Ctrl+C to stop the backend, or close this window." -ForegroundColor Yellow
            
            # Keep script alive and monitor backend
            try {
                while (-not $backendProcess.HasExited) {
                    Start-Sleep -Seconds 5
                    
                    # Periodically check health
                    if (-not (Test-BackendHealth)) {
                        Write-Warning "Backend health check failed - process may have crashed"
                    }
                }
            } catch {
                # User pressed Ctrl+C
                Write-Host ""
                Write-Status "Shutting down..."
                if (-not $backendProcess.HasExited) {
                    $backendProcess.Kill()
                }
            }
        }
        
        exit 0
    }
    
    Write-ProgressStep -Step 4 -Total 4 -Message "Launching frontend"
    Write-Host ""
    Write-Success "========================================"
    Write-Success "  Dirigent is ready." -ForegroundColor Green
    Write-Success "========================================"
    Write-Host ""
    Write-Status "Backend:  $HEALTH_URL"
    Write-Status "Logs:     $BACKEND_LOG"
    Write-Host ""
    
    # Launch frontend
    if (-not (Test-Path $FRONTEND_SCRIPT)) {
        Write-Error "Frontend launcher not found: $FRONTEND_SCRIPT"
        exit 1
    }
    
    Write-Status "Starting frontend..."
    Start-Process -FilePath $FRONTEND_SCRIPT -WorkingDirectory $PROJECT_ROOT
    
    Write-Success "Frontend launched successfully"
    Write-Host ""
    
    # Keep backend alive while frontend runs
    if (-not $backendAlreadyRunning) {
        Write-Host "Monitoring backend process (PID: $($backendProcess.Id))..." -ForegroundColor Gray
        Write-Host "Close this window to stop Dirigent." -ForegroundColor Yellow
        Write-Host ""
        
        try {
            while (-not $backendProcess.HasExited) {
                Start-Sleep -Seconds 5
                
                # Periodic health check
                if (-not (Test-BackendHealth)) {
                    Write-Warning "Backend health check failed - checking process..."
                    if ($backendProcess.HasExited) {
                        Write-Error "Backend process exited unexpectedly"
                        Write-Host "Check logs at: $BACKEND_LOG" -ForegroundColor Yellow
                        exit 1
                    }
                }
            }
            
            Write-Warning "Backend process exited"
            Write-Host "Check logs at: $BACKEND_LOG" -ForegroundColor Yellow
        } catch {
            # User closed window or pressed Ctrl+C
            Write-Host ""
            Write-Status "Shutting down Dirigent..."
            
            if (-not $backendProcess.HasExited) {
                Write-Status "Stopping backend process..."
                $backendProcess.Kill()
                $backendProcess.WaitForExit(5000) | Out-Null
            }
            
            if ($logTailJob.State -eq "Running") {
                Stop-Job $logTailJob | Out-Null
            }
            
            Write-Success "Dirigent stopped."
        }
    } else {
        Write-Host "Backend was already running. Frontend launched independently." -ForegroundColor Gray
        Write-Host "Close this window when done." -ForegroundColor Yellow
        
        try {
            while ($true) {
                Start-Sleep -Seconds 10
            }
        } catch {
            Write-Success "Dirigent launcher stopped."
        }
    }
    
} catch {
    Write-Error "Launcher failed: $_"
    Write-Host $_.ScriptStackTrace -ForegroundColor Red
    exit 1
}

